import getBit from './get-bit'

test('gets bits', () => {
	expect(getBit(0b1, 0)).toEqual(1)
	expect(getBit(0b0, 31)).toEqual(0)
	expect(getBit(0b11, 1)).toEqual(1)
	expect(getBit(0b10, 0)).toEqual(0)
	expect(getBit(0b101, 2)).toEqual(1)
	expect(getBit(0b110, 0)).toEqual(0)
	expect(getBit(0b1101, 3)).toEqual(1)
	expect(getBit(0b1000, 1)).toEqual(0)
	expect(getBit(0b10000, 4)).toEqual(1)
	expect(getBit(0b10000, 2)).toEqual(0)
	expect(getBit(0b100000, 5)).toEqual(1)
	expect(getBit(0b100000, 2)).toEqual(0)
	expect(getBit(0b1000000, 6)).toEqual(1)
	expect(getBit(0b1000000, 8)).toEqual(0)
	expect(getBit(0b10000000, 7)).toEqual(1)
	expect(getBit(0b10000000, 16)).toEqual(0)
	expect(getBit(0b1000000000000000000000000, 24)).toEqual(1)
	expect(getBit(0b1000000000000000000000000, 15)).toEqual(0)
	expect(getBit(0b1000000000000000000000000, 31)).toEqual(0)
	expect(getBit(0b10000000000000000000000000000000, 31)).toEqual(1)
})
