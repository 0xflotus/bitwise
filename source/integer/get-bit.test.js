import getBit from './get-bit'

test('gets bits', () => {
	expect(getBit(1, 0)).toEqual(1)
	expect(getBit(2, 1)).toEqual(1)
	expect(getBit(2, 0)).toEqual(0)
	expect(getBit(4, 2)).toEqual(1)
	expect(getBit(4, 0)).toEqual(0)
	expect(getBit(8, 3)).toEqual(1)
	expect(getBit(8, 1)).toEqual(0)
	expect(getBit(16, 4)).toEqual(1)
	expect(getBit(16, 2)).toEqual(0)
	expect(getBit(32, 5)).toEqual(1)
	expect(getBit(32, 2)).toEqual(0)
	expect(getBit(64, 6)).toEqual(1)
	expect(getBit(64, 8)).toEqual(0)
	expect(getBit(128, 7)).toEqual(1)
	expect(getBit(128, 16)).toEqual(0)
	expect(getBit(16777216, 24)).toEqual(1)
	expect(getBit(16777216, 15)).toEqual(0)
})
